    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
     // SAVE 
        if(
           !jTextField1.getText().trim().isEmpty()&&
           !jTextField2.getText().trim().isEmpty()&&
           !jTextField3.getText().trim().isEmpty())
     {
            int prod_id    = Integer.parseInt(jTextField1.getText().trim());
            String prod_price = jTextField2.getText().trim();
            String prod_name = jTextField3.getText().trim();
            
            List products = BakeryManagementSystemProject.products; 
           Product pr ;
        Boolean found = false;
        Iterator<Product> itr = BakeryManagementSystemProject.products.iterator();
        while (itr.hasNext()) {
            pr = itr.next();
            if (prod_id == pr.getProd_id()) {
                found = true;
                break;
            }
        }
        if (found) {
             // New Product Record  
             AddFunctions.add_product(prod_id, prod_price, prod_name);
             JOptionPane.showMessageDialog(null, "NEW Product Record Successfully ADDED!");
           } else {
             // found is true! So existing student recort will be updated!!!  
             EditFunctions.edit_product(prod_id, prod_price, prod_name);
             JOptionPane.showMessageDialog(null, "Product Record Successfully EDITED!");

           }
    }                                                                         
        else {
            JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
        }
        refresh_JTable();  
    }//GEN-LAST:event_save_btnActionPerformed

