     public void refresh_JTable() {    
           List products = BakeryManagementSystemProject.products;
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           Object rowData[] = new Object[3]; 
           Iterator itr = products.iterator();
           Product pr;          
           model.setNumRows(0); 
           while (itr.hasNext()) {
              pr = (Product) itr.next();
              rowData[0] = pr.getProd_id();
              rowData[1] = pr.getProd_price();
              rowData[2] = pr.getProd_name();
              model.addRow(rowData);
           }   
          }
